/**
 * Переменные строковые,метод charAT выводит символ по заданому индексу,
 * Синтаксист: имя_строки.charAT();
 */
public class Chs {

    public static void main(String[] args) {
        String str = "абсдеифчье";

        System.out.println("Это символ (" + str.charAt(7) + ")");

    }

}
